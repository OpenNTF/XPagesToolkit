package org.openntf.xpt.core.rest;

public class XPTRestException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public XPTRestException() {
		super();
	}

	public XPTRestException(String message, Throwable cause) {
		super(message, cause);
	}

	public XPTRestException(String message) {
		super(message);
	}

	public XPTRestException(Throwable cause) {
		super(cause);
	}

	
}
